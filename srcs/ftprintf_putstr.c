#include "ft_printf.h"

void	ftprintf_putstr(char const *s)
{
	int i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != '\0')
		{
			ftprintf_putchar(s[i]);
			i++;
		}
	}
}
